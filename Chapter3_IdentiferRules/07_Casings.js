// ===============================
// VARIABLE NAMING CASES
// ===============================

// 1. camelCase
// First word starts with lowercase,
// next words start with uppercase.
let studentName = "John";
let totalMarksObtained = 450;

// 2. snake_case
// All words are lowercase and separated by underscores (_).
let student_name = "John";
let total_marks_obtained = 450;

// 3. PascalCase
// Every word starts with an uppercase letter.
// Commonly used for classes and constructors.
let StudentName = "John";
let TotalMarksObtained = 450;

// 4. UPPER_SNAKE_CASE
// All letters are uppercase and words are separated by underscores.
// Commonly used for constants.
const MAX_USERS = 100;
const PI_VALUE = 3.14159;

// 5. kebab-case
// Uses hyphens (-) between words.
