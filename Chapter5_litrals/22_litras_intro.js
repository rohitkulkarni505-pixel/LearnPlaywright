// ===============================
// JAVASCRIPT LITERALS
// ===============================

// 1. Number Literal
let age = 25;
let price = 99.99;

// 2. String Literal
let firstName = "John";
let lastName = 'Doe';

// 3. Boolean Literal
let isLoggedIn = true;
let isAdmin = false;

// 4. Null Literal
let data = null;

// 5. Undefined Literal
let value = undefined;

// 6. Object Literal
let person = {
    name: "John",
    age: 25
};

// 7. Array Literal
let colors = ["red", "green", "blue"];

// 8. Function Literal (Function Expression)
const greet = function () {
    console.log("Hello");
};

// 9. Arrow Function Literal
const sayHi = () => {
    console.log("Hi");
};

// 10. Template Literal
let name = "John";
let message = `Hello ${name}`;

// 11. Regular Expression Literal
let pattern = /hello/i;

// 12. BigInt Literal
let bigNumber = 12345678901234567890n;

// 13. Symbol Literal (created using Symbol())
let id = Symbol("id");