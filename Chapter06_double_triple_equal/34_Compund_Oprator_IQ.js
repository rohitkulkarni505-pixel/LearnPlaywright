// ===================================
// ASSIGNMENT OPERATORS IN JAVASCRIPT
// ===================================

let x = 10;

// Addition Assignment Operator
x += 10;
console.log(x);
// Internally:
// x = x + 10;

// -----------------------------------

// Subtraction Assignment Operator
x -= 5;
console.log(x);
// Internally:
// x = x - 5;

// -----------------------------------

// Multiplication Assignment Operator
x *= 2;
console.log(x);
// Internally:
// x = x * 2;

// -----------------------------------

// Division Assignment Operator
x /= 3;
console.log(x);
// Internally:
// x = x / 3;

// -----------------------------------

// Modulus Assignment Operator
x %= 4;
console.log(x);
// Internally:
// x = x % 4;

// -----------------------------------

// Exponentiation Assignment Operator
x **= 2;
console.log(x);
// Internally:
// x = x ** 2;

// ===================================
// QUICK REFERENCE
// ===================================

// x += y  --> x = x + y
// x -= y  --> x = x - y
// x *= y  --> x = x * y
// x /= y  --> x = x / y
// x %= y  --> x = x % y
// x **= y --> x = x ** y


